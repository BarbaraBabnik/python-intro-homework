mood = input("What's your mood today? ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Don't worry! Everything will be ok!")
elif mood == "excited":
    print("Awesome!")
elif mood == "relaxed":
    print("Me too! Let's do nothing!")
else:
    print("I am not sure if I understood you. A re you sure you're feeling that way?")